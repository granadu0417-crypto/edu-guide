def get_content_type(filename):
    """파일명에서 콘텐츠 유형 파악"""
    if 'high-math' in filename:
        return 'high_math'
    elif 'high-english' in filename:
        return 'high_english'
    elif 'middle-math' in filename:
        return 'middle_math'
    elif 'middle-english' in filename:
        return 'middle_english'
    return 'unknown'


def get_intro_pool(content_type):
    """콘텐츠 유형에 따른 서두 풀 반환"""
    if content_type == 'high_math':
        return INTRO_POOL_HIGH_MATH
    elif content_type == 'high_english':
        return INTRO_POOL_HIGH_ENGLISH
    elif content_type == 'middle_math':
        return INTRO_POOL_MIDDLE_MATH
    elif content_type == 'middle_english':
        return INTRO_POOL_MIDDLE_ENGLISH
    return INTRO_POOL_HIGH_MATH


def rewrite_file(filepath, pool_index):
    """파일을 표현 풀을 사용하여 리라이팅"""
    try:
        with open(filepath, 'r', encoding='utf-8') as f:
            content = f.read()
    except:
        return False, "읽기 실패"

    filename = os.path.basename(filepath)
    content_type = get_content_type(filename)

    if content_type == 'unknown':
        return False, "알 수 없는 유형"

    # YAML과 본문 분리
    parts = content.split('---', 2)
    if len(parts) < 3:
        return False, "YAML 파싱 실패"

    yaml_content = parts[1]
    body_content = parts[2]

    # 지역 정보 추출
    city_match = re.search(r'cities:\s*\n\s*-\s*(\S+)', yaml_content)
    if not city_match:
        city_match = re.search(r'regions:\s*\n\s*-\s*(\S+)', yaml_content)
    city = city_match.group(1) if city_match else ""

    # 표현 풀 인덱스 계산 (다양하게 분산)
    idx1 = pool_index % 30
    idx2 = (pool_index * 7 + 3) % 20
    idx3 = (pool_index * 11 + 7) % 20
    idx4 = (pool_index * 13 + 11) % 20
    idx5 = (pool_index * 17 + 13) % 30

    # 서두 선택
    intro_pool = get_intro_pool(content_type)
    intro = intro_pool[idx1]

    # 아이보리 박스 선택
    box1 = IVORY_BOX_POOL[0][idx2]
    box2 = IVORY_BOX_POOL[1][idx3]
    box3 = IVORY_BOX_POOL[2][idx4]
    box4 = IVORY_BOX_POOL[3][(pool_index * 19 + 17) % 20]

    # 마무리 선택
    ending = ENDING_POOL[idx5]

    # 본문 내 아이보리 박스 교체
    box_pattern = r'(<div style="background-color: #FDF8F0[^>]*>\s*<strong>이렇게 수업합니다!</strong><br>\s*)([^<]+)(</div>)'

    box_replacements = [box1, box2, box3, box4]
    box_idx = [0]

    def replace_box(m):
        if box_idx[0] < len(box_replacements):
            replacement = box_replacements[box_idx[0]]
            box_idx[0] += 1
            return f'{m.group(1)}{replacement}\n{m.group(3)}'
        return m.group(0)

    body_content = re.sub(box_pattern, replace_box, body_content)

    # 서두 교체 (YAML 직후 첫 문단)
    first_para_match = re.match(r'(\s*\n)([^\n<#]+\n)', body_content)
    if first_para_match:
        body_content = f'\n{intro}\n' + body_content[first_para_match.end():]

    # 마무리 교체
    ending_pattern = r'(## 마무리\s*\n+)([^\n]+)'
    if re.search(ending_pattern, body_content):
        city_ending = f"{city} 학생 여러분, {ending}"
        body_content = re.sub(ending_pattern, f'\\g<1>{city_ending}', body_content)

    # 최종 콘텐츠 조합
    new_content = f'---{yaml_content}---{body_content}'

    # 파일 쓰기
    try:
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(new_content)
        return True, "성공"
    except:
        return False, "쓰기 실패"


def process_folder(folder_path, start_index=0):
    """폴더 내 모든 파일 처리"""
    files = []

    for root, dirs, filenames in os.walk(folder_path):
        for filename in filenames:
            if filename.endswith('.md') and not filename.startswith('_'):
                filepath = os.path.join(root, filename)
                files.append(filepath)

    print(f"  - {os.path.basename(folder_path)}: {len(files)}개 파일")

    success_count = 0
    fail_count = 0

    for idx, filepath in enumerate(files):
        success, msg = rewrite_file(filepath, start_index + idx)
        if success:
            success_count += 1
        else:
            fail_count += 1
            print(f"    실패: {filepath} - {msg}")

    return success_count, fail_count, len(files)


