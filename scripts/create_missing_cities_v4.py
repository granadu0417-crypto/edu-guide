def add_extra_content(base_content, subject, target_lines, ivory):
    """목표 줄 수에 맞게 추가 콘텐츠를 삽입"""
    current_lines = base_content.count('\n') + 1
    sub = "math" if subject == "math" else "english"

    extra_list = EXTRA_SECTIONS[sub].copy()
    random.shuffle(extra_list)

    cta_marker = "{{< cta-dual"
    ivory_idx = 7

    for title, content_block in extra_list:
        if current_lines >= target_lines - 10:
            break

        cta_pos = base_content.find(cta_marker)
        if cta_pos > 0:
            # 아이보리 박스 추가
            extra_text = title + content_block
            if ivory_idx < len(ivory):
                extra_text += make_ivory_box(ivory[ivory_idx]) + "\n\n"
                ivory_idx += 1

            base_content = base_content[:cta_pos] + extra_text + base_content[cta_pos:]
            current_lines = base_content.count('\n') + 1

    return base_content

def main():
    total_files = 0
    line_stats = {"200-250": 0, "251-300": 0, "301-350": 0, "351-400": 0}

    for city_id, city_info in CITIES.items():
        city_path = os.path.join(BASE_PATH, city_id)
        print(f"\n=== {city_info['name_ko']} ({city_id}) ===")

        file_index = 0
        for dong in city_info["dongs"]:
            dong_path = os.path.join(city_path, dong)
            os.makedirs(dong_path, exist_ok=True)

            index_file = os.path.join(dong_path, "_index.md")
            with open(index_file, 'w', encoding='utf-8') as f:
                f.write(f'''---
title: "{dong} 과외"
---
''')

            for level in ["middle", "high"]:
                for subject in ["math", "english"]:
                    target_lines = get_target_lines()

                    # 기본 콘텐츠 생성
                    base_content = generate_base_content(
                        city_info['name_ko'], dong, subject, level, file_index
                    )

                    # 추가 콘텐츠로 목표 줄 수 맞추기
                    ivory = random.sample(IVORY_BOXES, len(IVORY_BOXES))
                    final_content = add_extra_content(base_content, subject, target_lines, ivory)

                    filename = f"{level}-{subject}.md"
                    filepath = os.path.join(dong_path, filename)

                    with open(filepath, 'w', encoding='utf-8') as f:
                        f.write(final_content)

                    actual_lines = final_content.count('\n') + 1

                    if actual_lines <= 250:
                        line_stats["200-250"] += 1
                    elif actual_lines <= 300:
                        line_stats["251-300"] += 1
                    elif actual_lines <= 350:
                        line_stats["301-350"] += 1
                    else:
                        line_stats["351-400"] += 1

                    total_files += 1
                    file_index += 1

            print(f"  {dong}: 4개 파일 생성")

    print(f"\n=== 완료 ===")
    print(f"총 {total_files}개 파일 생성")
    print(f"\n줄 수 분포:")
    for range_name, count in line_stats.items():
        pct = count / total_files * 100 if total_files > 0 else 0
        print(f"  {range_name}줄: {count}개 ({pct:.1f}%)")

if __name__ == "__main__":
    main()
