def get_pool_item(pool, index):
    """인덱스 기반으로 풀에서 항목 선택 (순환)"""
    return pool[index % len(pool)]

def get_unique_combination(pools, index, offset_multiplier=7):
    """여러 풀에서 겹치지 않는 조합 선택"""
    results = []
    for i, pool in enumerate(pools):
        offset = index + (i * offset_multiplier)
        results.append(pool[offset % len(pool)])
    return results
