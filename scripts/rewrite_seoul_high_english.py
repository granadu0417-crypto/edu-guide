    base_path = "/home/user/edu-guide/content/seoul"
    gu_folders = [d for d in os.listdir(base_path) if os.path.isdir(os.path.join(base_path, d))]

    total = 0
    pool_index = 0

    for gu_folder in sorted(gu_folders):
        gu_path = os.path.join(base_path, gu_folder)
        files = glob.glob(os.path.join(gu_path, "*-high-english.md"))

        for filepath in sorted(files):
            if filepath.endswith("/high-english.md"):
                continue
