def extract_info_from_file(filepath):
    """기존 파일에서 구, 동, 학교 정보 추출"""
    try:
        with open(filepath, 'r', encoding='utf-8') as f:
            content = f.read()

        gu_match = re.search(r'cities:\s*\n\s*-\s*(\S+)', content)
        gu_korean = gu_match.group(1) if gu_match else ""

        title_match = re.search(r'title:\s*"([^"]+구)\s+(\S+)\s+(고등|중등)', content)
        dong_korean = title_match.group(2) if title_match else ""

        school_match = re.search(r'title:\s*"[^|]+\|\s*([^"]+)"', content)
        if school_match:
            school_part = school_match.group(1)
            school_name_match = re.match(r'([^\s]+고[^\s]*)', school_part)
            school_name = school_name_match.group(1) if school_name_match else school_part.split()[0]
        else:
            school_name = ""

        image_match = re.search(r'featured_image:\s*"([^"]+)"', content)
        featured_image = image_match.group(1) if image_match else "/images/edu_0001.jpg"

        return gu_korean, dong_korean, school_name, featured_image
    except:
        return None, None, None, None


