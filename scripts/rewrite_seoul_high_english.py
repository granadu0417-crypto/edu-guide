def rewrite_file(filepath, pool_index):
    """파일 리라이팅"""
    gu_korean, dong_korean, school_name, featured_image = extract_info_from_file(filepath)

    if not all([gu_korean, dong_korean, school_name]):
        print(f"⚠ {filepath} 정보 추출 실패")
        return False

    content = generate_content(gu_korean, dong_korean, school_name, featured_image, pool_index)

    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(content)

    print(f"✓ {gu_korean} {dong_korean} 영어 리라이팅 완료")
    return True


