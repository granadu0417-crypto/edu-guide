def generate_district_content(dongs, district_name, district_id, edu_office, output_dir):
    created_files = []

    for i, dong in enumerate(dongs):
        # 수학 파일 생성
        math_filename = f"{district_id}-{dong['id']}-middle-math.md"
        math_filepath = os.path.join(output_dir, math_filename)
        math_content = create_math_content(dong, i, district_name, district_id, edu_office)

        with open(math_filepath, 'w', encoding='utf-8') as f:
            f.write(math_content)
        created_files.append(math_filename)

        # 영어 파일 생성
        english_filename = f"{district_id}-{dong['id']}-middle-english.md"
        english_filepath = os.path.join(output_dir, english_filename)
        english_content = create_english_content(dong, i, district_name, district_id, edu_office)

        with open(english_filepath, 'w', encoding='utf-8') as f:
            f.write(english_content)
        created_files.append(english_filename)

    return created_files


