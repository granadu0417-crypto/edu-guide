def enrich_file_content(file_path):
    """파일 콘텐츠 보강"""
    try:
        with open(file_path, 'r', encoding='utf-8') as f:
            content = f.read()

        # Front matter 분리
        if not content.startswith('---'):
            return False, "Front matter 없음"

        parts = content.split('---', 2)
        if len(parts) < 3:
            return False, "Front matter 형식 오류"

        front_matter = parts[1]
        old_body = parts[2]

        # 제목 추출
        title_match = re.search(r'^title:\s*"([^"]*)"', front_matter, re.MULTILINE)
        if not title_match:
            return False, "제목 없음"

        title = title_match.group(1)

        # 카테고리 파악
        rel_path = file_path.relative_to(Path('content'))
        category = str(rel_path).split('/')[0]
        filename = file_path.stem

        # _index.md 파일인 경우
        if filename == '_index':
            new_body = generate_index_content(category, title)
        # elementary 파일
        elif 'elementary' in str(rel_path):
            new_body = generate_elementary_content(title, filename)
        # middle 파일
        elif 'middle' in str(rel_path):
            new_body = generate_middle_content(title, filename)
        # high 파일
        elif 'high' in str(rel_path):
            new_body = generate_high_content(title, filename)
        # 기타 _index.md
        else:
            new_body = generate_index_content(category, title)

        # 새 콘텐츠 생성
        new_content = f"---{front_matter}---{new_body}"

        # 파일 저장
        with open(file_path, 'w', encoding='utf-8') as f:
            f.write(new_content)

        return True, f"{category}/{filename}"

    except Exception as e:
        return False, f"오류: {e}"

